package com.example.quotes.Modal;

public class Modal_Data {
    String s;
    int image;


    public Modal_Data(String s, int image) {
        this.s = s;
        this.image = image;

    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
